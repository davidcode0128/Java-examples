public class Ex01字元字串 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        System.out.println("哈囉");
        System.out.println("100"); // ""字串 資料類型
        System.out.println(100); // 整數 資料類型
        System.out.println("100" + "99");
        System.out.println(100 + 99);
        System.out.println((100 + 99)/ 2); //整數運算 結果為整數
        System.out.println((100 + 99)/2.0); //浮點運算 結果為浮點數(近似值)
        System.out.println(0.1); //浮點數(近似值)
    }
    
}
